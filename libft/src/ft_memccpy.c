#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	char	*tmp;
	size_t	i;

	i = -1;
	tmp = (char *)dst;
	while (++i < n)
	{
		*((char *)dst + i) = *((unsigned char *)src + i);
		if (*((unsigned char *)src + i) == (unsigned char)c)
			return (dst + i + 1);
	}
	return (0);
}
